package Activities;

public class IncrementDecrementOperator {

	public static void main(String[] args) {
		int m=5;
		
		
		System.out.println("Post Incremented value of m is " + (m++));
		System.out.println("The value of m is " + (m));
		System.out.println("Pre Incremented value of m is " + (++m));
		System.out.println("The value of m is " + (m));
		System.out.println("Post decremented value  of m is " + (m--));
		System.out.println("The value is  of n " + (m));
		System.out.println("Post Incremented value of m is " + (--m));
		System.out.println("The value is  of n " + (m));
		
		
		
	}

}

