package MyCode;

public class UnaryOperator {

	public static void main(String[] args) {
		int m=9;
		int n=14;
		
		System.out.println("Post Incremented value of m is " + (m++));
		System.out.println("The value of m is " + (m));
		System.out.println("Pre Incremented value of m is " + (++m));
		System.out.println("Pre Incremented value  of n is " + (++n));
		System.out.println("The value is  of n " + (n));
		System.out.println("Post decremented value  of n is " + (n--));
		System.out.println("The value is  of n " + (n));
		System.out.println("Post Incremented value of n is " + (++n));
		
		
		
		
	}

}
